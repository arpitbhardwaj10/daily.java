package VariableDeclaration;

//Write a Java program to declare variables of types int, float, double, char, and boolean, assign values to them, and print them.

public class Basic1 {
    public static void main (String[]args){
        int a = 22;

        float b = 2.222222f;

        double c = 2.2;
        
        char d = 'C';

        boolean e = true;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }

    
}
