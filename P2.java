

//WAJP to store int value inside the byte,short,long,float,double and char type of variable.

public class P2 {
    public static void main(String[]args){
        byte a = 2;
        int b = (int)a;
        System.out.println(b);

        short c = 32;
        int d = (int)c;
        System.out.println(d);

        long e = 1234567890098765l;
        int f = (int)e;
        System.out.println(f);

        double g = 24.4;
        int h = (int)g;
        System.out.println(h);

        char i = 'a';
        int j = (int)i;
        System.out.println(j); 
    }
    

}
