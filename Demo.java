package VariableDeclaration;

public class Demo {
    public static void main(String[]args){
        int a = 20;
        System.out.println(a);

        double b = 20.1;
        System.out.println(b);

        char c = 'A';
        System.out.println(c);

        boolean d = true;
        System.out.println(d);

        String s = "Arpit";
        System.out.println(s);
    }
    
}
